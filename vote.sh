#! /usr/bin/bash

echo "*************************************************************************************"
echo "--------------------------Welcome to Voting Age Calculator---------------------------"
echo "*************************************************************************************"


echo "Enter You Age in YYYY-MM-DD Format"
read dob


dob_sec=$(date -d "$dob" +%s)
today_sec=$(date +%s)

diff=$((today_sec-dob_sec))
age=$((diff / 31536000))

echo "***********************************************************************************"
echo "--------------------------------Your age is : $age---------------------------------"
echo "************************************************************************************"
if (($age > 18))
then
echo "************************************************************************************"
echo " -----------------------------You are Eligble to vote-------------------------------"
echo "************************************************************************************"

else 

echo "*************************************************************************************"
echo "-----------------------------You are Not Eligible to vote-----------------------------"
echo "*************************************************************************************"

fi


